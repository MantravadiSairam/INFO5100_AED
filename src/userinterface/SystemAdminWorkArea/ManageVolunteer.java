    private void btnajModifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnajModifyActionPerformed
            int selectedRow = jTable1.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(null,"Please Select a row to update");
            return;
        }
        Nurse nurse = (Nurse)jTable1.getValueAt(selectedRow,0);
        
        ModifyVolunteer modifyVolunteer = new ModifyVolunteer(userProcessContainer, ecosystem, nurseDirectory,nurse);
        userProcessContainer.add("ModifyVolunteer", modifyVolunteer);
        CardLayout layout=(CardLayout)userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btnajModifyActionPerformed

    private void btnjDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnjDeleteActionPerformed
            int selectedRow = jTable1.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(null,"Select a row to delete");
            return;
        }
        
         Nurse nurse = (Nurse)jTable1.getValueAt(selectedRow,0);
        nurseDirectory.removeNurse(nurse);
        populateTable();
    }//GEN-LAST:event_btnjDeleteActionPerformed


