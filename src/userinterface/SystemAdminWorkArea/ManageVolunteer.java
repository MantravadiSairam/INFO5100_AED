     public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for(Nurse nurse : ecosystem.getNurseDirectory().getNurseDirectory()){
            Object [] row = new Object[6];
            row[0] = nurse;
            row[1] = nurse.getName();
            row[2] = nurse.getGender();
            
            row[3] = nurse.getAddress();
            row[4] = nurse.getEmail();
            row[5] = nurse.getMobile();
           
            
            model.addRow(row);
        }
    }
