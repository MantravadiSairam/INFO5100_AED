    private void addjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addjButtonActionPerformed
        // TODO add your handling code here:
        String mobile = mobilejText.getText();
        String email = emailjText.getText();
        String userName = unText.getText();
        String password = passwordField.getText();

        for(DeliveryMan deliveryMan : deliveryManDirectory.getDeliveryManDirectory()) {

            if(deliveryMan.getPhoneNumber().equals(mobile)) {
                JOptionPane.showMessageDialog(null, "Mobile Number already exists");
            }
        }
        for(DeliveryMan deliveryMan : deliveryManDirectory.getDeliveryManDirectory()) {

            if(deliveryMan.getEmail().equals(email)) {
                JOptionPane.showMessageDialog(null, "Email already exists");
            }
        }

        for(UserAccount account : ecoSystem.getUserAccountDirectory().getUserAccountList()) {
            if(account.getUsername().equals(userName)) {
                JOptionPane.showMessageDialog(null, "Username Already exists");
                return;
            }
        }

        if (namejText.getText().isEmpty() || !namejText.getText().matches("^[a-zA-Z]+(?:[\\s-][a-zA-Z]+)*$"))
        JOptionPane.showMessageDialog(null, "Please enter valid name");

        else if(!(mobilejText.getText().length()==10) || !mobilejText.getText().matches("^[0-9]+") || mobilejText.getText().matches("[0]{10}"))
        JOptionPane.showMessageDialog(null, "enter valid mobile number");

        else if(emailjText.getText().isEmpty()|| !(emailjText.getText().trim().contains("@") && emailjText.getText().trim().contains(".")))
        JOptionPane.showMessageDialog(null, "Enter valid email address");

        else if (addText.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter address");
        }

        else if (userName.isEmpty() || !userName.matches("^[a-zA-Z0-9]*$"))
        JOptionPane.showMessageDialog(null, "Please enter userName");

        else if (password.isEmpty() || !password.matches("^[a-zA-Z0-9]*$"))
        JOptionPane.showMessageDialog(null, "Please enter password");

        else
        {
            JOptionPane.showMessageDialog(null, "Delivery Person Profile Created");

            ecoSystem.getDeliverymanDirectory().addDeliveryMan(namejText.getText(), addText.getText(), email, mobile);
            //Employee employee = ecoSystem.getEmployeeDirectory().createEmployee(nameText.getText());
            //UserAccount userAccount = ecoSystem.getUserAccountDirectory().createUserAccount(userName, password, employee, new CustomerRole());
            Employee employee = ecoSystem.getEmployeeDirectory().createEmployee(email);
            ecoSystem.getUserAccountDirectory().createUserAccount(userName, password, employee, new DeliveryManRole());

        }
    }//GEN-LAST:event_addjButtonActionPerformed

