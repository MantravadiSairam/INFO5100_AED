    private void updatejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatejButtonActionPerformed
        // TODO add your handling code here:
         String RestName = namejText.getText();
        String address = addjText.getText();
        String phone = mobilejText.getText();
        String ManName = emailjText.getText();
        if(RestName.isEmpty() || ManName.isEmpty() ||
            phone.isEmpty() || address.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Fields cannot be left empty");
            return;
        }

        boolean flag ;

        //String phoneNumber = phone;

        if(phone.length() != 10) {
            JOptionPane.showMessageDialog(null, "PhoneNumber must be of 10 digits");
            return;
        }
        flag = phone.matches("^[0-9]+$");
        if(!flag) {
            JOptionPane.showMessageDialog(null, "Phone Number must have digits only");
            return;
        }
        for(Pharmacy res : pharmacyDirectory.getPharmacyDirectory()) {
            if(res.getPhone().equals(phone) && this.pharmacy != res) {
                JOptionPane.showMessageDialog(null, "Phone Number already exists");
                return;
            }
        }

        //String address = txtAddress.getText();

        pharmacy.setPharmName(RestName);
        pharmacy.setPharmacist(ManName);
        pharmacy.setPhone(phone);
        pharmacy.setAddress(address);

        JOptionPane.showMessageDialog(null, "Food Source profile Updated");
    }//GEN-LAST:event_updatejButtonActionPerformed

