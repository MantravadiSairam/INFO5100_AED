    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for(NGO NGO : ecosystem.getHelperDirectory().getHelperDirectory()){
            Object [] row = new Object[6];
            row[0] = NGO;
            row[1] = NGO.getName();
            row[2] = NGO.getProfession();
            row[3] = NGO.getMobile();
            row[4] = NGO.getEmail();
            row[5] = NGO.getAddress();
            
            model.addRow(row);
        }
    }

