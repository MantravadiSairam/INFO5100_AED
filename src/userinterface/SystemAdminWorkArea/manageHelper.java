    private void updatejjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatejjButtonActionPerformed
        // TODO add your handling code here:
            int selectedRow = jTable1.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(null,"Please Select a row to update");
            return;
        }
        NGO help = (NGO)jTable1.getValueAt(selectedRow,0);
        
        updateNGO updateNGO = new updateNGO(userProcessContainer, ecosystem, helperDirectory,help);
        userProcessContainer.add("updtaeHelperJPanel", updateNGO);
        CardLayout layout=(CardLayout)userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_updatejjButtonActionPerformed

