 public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) jjTable1.getModel();
        dtm.setRowCount(0);
        
        for(GOrder order : ecoSystem.getgOrderDirectory().getOrderDirectory()) {
            System.out.println(" Rest  " + account.getEmployee().getName() + "  " + order.getGrocer().getGrocerName());
            if(account.getEmployee().getName().equals(order.getGrocer().getGrocerName())) {
                Object [] row = new Object[5];
                row[0] = order;
                row[1] = order.getMenu().getItemName();
                row[2] = order.getQuantity();
                row[3] = order.getQuantity() * order.getMenu().getPrice();
                row[4] = order.getOrderStatus();
                dtm.addRow(row);
            }
        }
    }
