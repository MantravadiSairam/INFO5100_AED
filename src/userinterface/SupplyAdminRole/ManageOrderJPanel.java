    private void OAjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OAjButtonActionPerformed
        // TODO add your handling code here:
         int selectedRow = jjTable1.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(null,"Please Select a row from table first", "Warining", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        GOrder order = (GOrder)jjTable1.getValueAt(selectedRow, 0);
        if(order.getOrderStatus().equals("Order Placed")) {
            order.setOrderStatus("Order Accepted");
        }
        
        else {
            JOptionPane.showMessageDialog(null, "Order is already accepted or Completed");
            return;
        }
        
        populateTable();
    }//GEN-LAST:event_OAjButtonActionPerformed

    private void OCjButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OCjButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jjTable1.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(null,"Please Select a row from table first", "Warining", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        GOrder order = (GOrder)jjTable1.getValueAt(selectedRow, 0);
        if(order.getOrderStatus().equals("Order Accepted")) {
            order.setOrderStatus("Order Ready for Pick up");
            order.setAllot(true);
        }
        
        else if(order.getOrderStatus().equals("Order Placed")) {
            JOptionPane.showMessageDialog(null, "Order is should be accepted first");
            return;
        }
        
        else {
            JOptionPane.showMessageDialog(null, "Order is already ready for pick up");
            return;
        }
        
        populateTable();
    }//GEN-LAST:event_OCjButton1ActionPerformed

